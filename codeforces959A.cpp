#include <bits/stdc++.h>
using namespace std;

int main()
{
    // taking input
    int number;
    cin >> number;

    // output
    if (number % 2 == 0)
        cout << "Mahmoud" << endl;
    else
        cout << "Ehab" << endl;

    return 0;
}